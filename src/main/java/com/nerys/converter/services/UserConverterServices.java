package com.nerys.converter.services;

import com.avbravo.jmoordbutils.FacesUtil;
import com.nerys.model.User;
import com.nerys.services.UserServices;
import jakarta.faces.view.ViewScoped;
import jakarta.inject.Inject;
import jakarta.inject.Named;
import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;
import java.util.Optional;

/**
 *
 * @author avbravo
 */
@ViewScoped
@Named
public class UserConverterServices implements Serializable {

    @Inject
    UserServices userServices;

    List<User> users = new ArrayList<>();

    public List<User> getUsers() {
        return users;
    }

    public void setUsers(List<User> users) {
        this.users = users;
    }

    // <editor-fold defaultstate="collapsed" desc="Optional<User> get(Long id)">
    public Optional<User> get(Long id) {
        Optional<User> result;

        try {
            result = users.stream().filter(x -> x.getIduser().equals(id)).findFirst();
            if (!result.isPresent()) {
                Optional<User> user = userServices.findByIduser(id);
                if (user.isPresent()) {
                    users.add(user.get());
                    return user;
                }
                result = Optional.empty();
            }
            return result;
        } catch (Exception e) {

            FacesUtil.errorMessage(FacesUtil.nameOfClassAndMethod() + " " + e.getLocalizedMessage());
        }
        return Optional.empty();

    }
// </editor-fold>
    // <editor-fold defaultstate="collapsed" desc="void add(List<User> users)">

    public void add(List<User> users) {
        try {
            destroyed();
            this.users = users;
        } catch (Exception e) {

            FacesUtil.errorMessage(FacesUtil.nameOfClassAndMethod() + " " + e.getLocalizedMessage());
        }

    }
// </editor-fold>

// <editor-fold defaultstate="collapsed" desc="void destroyed()">
    public void destroyed() {
        try {
            this.users = new ArrayList<>();
        } catch (Exception e) {

            FacesUtil.errorMessage(FacesUtil.nameOfClassAndMethod() + " " + e.getLocalizedMessage());
        }

    }
// </editor-fold>

}
